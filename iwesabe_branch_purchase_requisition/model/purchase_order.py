from odoo import models, api, fields, _
from odoo.exceptions import UserError


class PurchaseOrder(models.Model):
    _inherit = 'purchase.order'

    # moved from iwesabe_branch_purchase purchase order by Shivam on 04-01-2022
    @api.onchange('requisition_id')
    def _onchange_requisition_id(self):
        super(PurchaseOrder, self)._onchange_requisition_id()
        if self.requisition_id.branch_id:
            self.branch_id = self.requisition_id.branch_id.id